
/**
* Function that calculates the check bit for an Israeli ID number
* @param 8digit ID Number (without the check bit)
* @return the check bit
*/
int calcCheckBit(int id);

/**
* Function that sums the numbers while doubling every other number
* @param integer number
* @return a sum according to above specifications
*/
int sumOfNumbers(int num);

/**
* Function that calculates how many digits an intger has
* @param integer number
* @return amount of digits in the number
*/
int numLength(int num);

/**
* Function that combines two digit numbers to a one digit number (by adding them together)
* @param 2 digit number
* @return 1 digit number
*/
int combineDigits(int num);